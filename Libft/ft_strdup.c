#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	char	*src;
	size_t	i;

	src = (char *)s1;
	dest = (char *) malloc(ft_strlen(src + 1));
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = 0;
	return (dest);
}
